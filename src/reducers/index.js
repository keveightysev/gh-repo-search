export const repoSearchReducer = (state, action) => {
  const { type, payload } = action;
  switch (type) {
    case "SET_SEARCH_TERM":
      return {
        ...state,
        searchTerm: payload
      };
    case "FETCH_START":
      return {
        ...state,
        isFetching: true,
        error: false,
        items: [],
        totalCount: 0
      };
    case "FETCH_SUCCESS":
      return {
        ...state,
        isFetching: false,
        items: payload.items,
        totalCount: payload.totalCount
      };
    case "FETCH_FAILURE":
      return {
        ...state,
        isFetching: false,
        error: true
      };
    case "SET_LIMIT":
      return {
        ...state,
        resultLimit: payload
      };
    default:
      return state;
  }
};
